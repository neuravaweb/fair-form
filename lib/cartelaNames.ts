/**
 * Get the display name for a cartela number in a collection
 * @param collection - Collection name
 * @param cartelaNumber - Cartela number
 * @returns Display name or the number if no name is found
 */
export function getCartelaName(collection: string, cartelaNumber: number): string {
  const collectionNames = cartelaNames[collection]
  if (collectionNames && collectionNames[cartelaNumber]) {
    return collectionNames[cartelaNumber]
  }
  return cartelaNumber.toString()
}

/**
 * Get all cartela names for a collection
 * @param collection - Collection name
 * @returns Record of cartela number to name mapping
 */
export function getCollectionCartelaNames(collection: string): Record<number, string> {
  return cartelaNames[collection] || {}
}
